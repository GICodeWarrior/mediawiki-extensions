	private static function stringToList( $str ) {
		$defaultEncoding = mb_internal_encoding();
		if( !mb_internal_encoding( 'utf-8' ) ) {
			die( "Can't set utf-8\n" );
		}
		$out = array();
		$len = mb_strlen( $str );
		for( $i = 0; $i < $len; $i++ ) {
