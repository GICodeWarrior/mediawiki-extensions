CREATE TABLE halfak.project_pages
SELECT ppg.title_prefix, ppg.type, p.* FROM page p
INNER JOIN halfak.project_page_group ppg ON
	p.page_title LIKE CONCAT(ppg.title_prefix, "%") AND
	p.page_namespace IN (4,5);
